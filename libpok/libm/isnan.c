/*
 * isnan(x) returns 1 is x is nan, else 0;
 * no branching!
 */

#ifdef POK_NEEDS_LIBMATH
#include "math_private.h"
#include <types.h>

int isnan(double x) {
  int32_t hx, lx;
  EXTRACT_WORDS(hx, lx, x);
  hx &= 0x7fffffff;
  hx |= (uint32_t)(lx | (-lx)) >> 31;
  hx = 0x7ff00000 - hx;
  return (int)((uint32_t)(hx)) >> 31;
}
#endif
